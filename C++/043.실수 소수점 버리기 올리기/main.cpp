#include <iostream>
#include <math.h>

using namespace std;

int main()
{
    cout << "== 소수점 버리기 ==" << endl;
    cout << "==floor==" << endl;
    cout << floor(1.1) << endl;
    cout << floor(-2.3) << endl;

    cout << "==소수점 올리기==" << endl;
    cout << "==ceil==" << endl;
    cout << ceil(1.1) << endl;
    cout << ceil(-2.3) << endl;

    return 0;
}